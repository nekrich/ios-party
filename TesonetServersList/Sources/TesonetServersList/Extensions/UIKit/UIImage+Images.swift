import UIKit

extension UIImage {
	
	final class var loginUsernameIco: UIImage {
		return #imageLiteral(resourceName: "login.Username")
	}
	
	final class var loginPasswordIco: UIImage {
		return #imageLiteral(resourceName: "login.Password")
	}
	
	final class var logoutIcon: UIImage {
		return #imageLiteral(resourceName: "logoutIcon")
	}
	
	final class var logoDark: UIImage {
		return #imageLiteral(resourceName: "logo.Dark")
	}
	
}
